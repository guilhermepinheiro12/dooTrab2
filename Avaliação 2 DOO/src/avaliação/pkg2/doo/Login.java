    private void btt_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btt_loginMouseClicked
        
        /*
            Essa função faz a 'proteção' da aplicação, sendo requerido um login 
        e senha específico para acessar o conteúdo da aplicação.
        */
        
        String id = "", password = "";
        
        id = id_form.getText();
        password = pass_form.getText();
        
        /*
            Se for verificado como correto o login e senha, então será passado para 
        a próxima janela, que dará opções para utilização da aplicação, e fará com 
        que essa página seja fechada.
        */
        
        if ( (id.equals("adm") == true) && (password.equals("123")) ) {
            JFrame menu = new Menu();
            menu.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Usuário e/ou senha incorreta!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btt_loginMouseClicked

